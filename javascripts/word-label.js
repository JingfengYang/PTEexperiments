    var nodes = {};

    var maxWeight = 0;

    data.forEach(function(d) {
        d.source = nodes[d.source] || (nodes[d.source] = {name: d.source});
        d.target = nodes[d.target] || (nodes[d.target] = {name: d.target});
        if (d.weight > maxWeight) {
            maxWeight = d.weight;
        }
    });

    labels.forEach(function(d) {
        d.source = nodes[d.source] || (nodes[d.source] = {name: d.source});
        d.target = nodes[d.target] || (nodes[d.target] = {name: d.target});
    })

    var widthScale = d3.scaleLinear().domain(1, maxWeight).range([1, 5]);
    var width = window.innerWidth * 4 / 5,
        height = window.innerHeight * 4 / 4;

    var force = d3.forceSimulation()
                  .nodes(d3.values(nodes))
                  .force("link", d3.forceLink(data).distance(150))
                  .force("link", d3.forceLink(labels).distance(150))
                  .force('center', d3.forceCenter(width / 2, height / 2))
                  .force("x", d3.forceX())
                  .force("y", d3.forceY())
                  .force("charge", d3.forceManyBody().strength(-100))
                  .alphaTarget(1)
                  .on("tick", tick);

    var svg = d3.select("#word-label").append("svg")
                .attr("width", width)
                .attr("height", height);

    var path2 = svg.append("g")
                 .selectAll("path")
                 .data(labels)
                 .enter()
                 .append("path")
                 .attr("stroke", "#FFC300")
                 .attr("stroke-width", 1)
                 .attr("fill", "none");

    var node2 = svg.selectAll(".node")
     .data(force.nodes())
     .enter().append("g")
     .attr("class", "node")
     .call(d3.drag()
     .on("start", dragstarted)
     .on("drag", dragged)
     .on("end", dragended)
    );

    node2.append("circle")
     .attr("r", 13)
     .attr("fill", "white");

    node2.append("text")
     .style("text-anchor", "middle")
     .text(function(item, index, array) {return item.name})
     .attr("fill", "black")
     .attr("font-size", 10)
     .attr("font-family", "sans-serif");

    var path = svg.append("g")
                 .selectAll("path")
                 .data(data)
                 .enter()
                 .append("path")
                 .attr("stroke", "steelblue")
                 .attr("stroke-width", function(d) { return widthScale(d.weight)})
                 .attr("fill", "none");

    var node = svg.selectAll(".node")
     .data(force.nodes())
     .enter().append("g")
     .attr("class", "node")
     .call(d3.drag()
     .on("start", dragstarted)
     .on("drag", dragged)
     .on("end", dragended)
    );

    node.append("circle")
     .attr("r", 13)
     .attr("fill", "white");

    node.append("text")
     .style("text-anchor", "middle")
     .text(function(item, index, array) {return item.name})
     .attr("fill", "black")
     .attr("font-size", 10)
     .attr("font-family", "sans-serif");

    function tick() {
     path.attr("d", function(d) {
         var dx = d.target.x - d.source.x,
             dy = d.target.y - d.source.y;
         return "M" + d.source.x + " " + d.source.y + " " + "L" + d.target.x + " " + d.target.y;
     });

     path2.attr("d", function(d) {
         var dx = d.target.x - d.source.x,
             dy = d.target.y - d.source.y;
         return "M" + d.source.x + " " + d.source.y + " " + "L" + d.target.x + " " + d.target.y;
     });

     node.attr("transform", function(d) {
     return "translate(" + d.x + "," + d.y + ")"; })

     node2.attr("transform", function(d) {
     return "translate(" + d.x + "," + d.y + ")"; })
    };

    function dragstarted(d) {
     if (!d3.event.active) force.alphaTarget(0.3).restart();
     d.fx = d.x;
     d.fy = d.y;
    };

    function dragged(d) {
     d.fx = d3.event.x;
     d.fy = d3.event.y;
    };

    function dragended(d) {
     if (!d3.event.active) force.alphaTarget(0);
     if (d.fixed == true) { d.fx = d.x; d.fy = d.y; }
     else { d.fx = null; d.fy = null; }
    };